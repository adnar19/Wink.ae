                <h2>Qu’est-ce qu’un cookie ?</h2>
                <p>Un « cookie » ou un « traceur » est un moyen technologique permettant des opérations visant à
                    accéder, par voie de transmission électronique, à des informations déjà stockées sur un terminal
                    d’accès (ex. un ordinateur, une tablette, un smartphone), qui peuvent être ou non des données à
                    caractère personnel.</p>
                <p>Il sera conservé pendant une certaine durée dans votre terminal d’accès et pourra être lu par le site
                    Internet ou par l’application chaque fois que vous vous y connecterez.</p>
                <p>Les traceurs ont de multiples usages. Ils nous permettent notamment de vous procurer une expérience
                    complète et personnalisée, ainsi que d’effectuer des analyses statistiques et de performance pour
                    améliorer la qualité de nos services.</p>
                <p>Notre site web et nos applications mobiles n’affichent pas de publicité. En conséquence, nous ne
                    plaçons jamais de traceurs à des fins publicitaires sur votre terminal d’accès.</p>
                <h2>Quels sont les types de cookies utilisés par Wink AE?</h2>
                <p>Nous utilisons des cookies internes, placés directement par nos soins, et des cookies tiers, placés
                    par nos partenaires.</p>
                <p>Il existe des cookies de session et des cookies persistants. Les cookies de session ne durent que
                    jusqu'à la fermeture de votre navigateur. Les cookies persistants ont une durée de vie plus longue
                    et permettent de mémoriser des informations d'une visite à une autre. Les cookies persistants que
                    nous utilisons ont une durée de vie maximale de 13 mois, conformément à la réglementation
                    applicable.</p>
                <p>Nous utilisons quatre types de cookies dont les finalités sont décrites ci-après.</p>

