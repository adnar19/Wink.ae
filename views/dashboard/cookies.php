                <p>Ils nous permettent donc de vous proposer nos services et d’améliorer votre expérience en tant
                    qu’utilisateur.</p>

                <h2>Les cookies analytiques</h2>
                <p>Ces cookies nous permettent de recueillir des informations concernant la façon dont nos services sont
                    utilisés par nos membres (par exemple, pour identifier les pages que l’Utilisateur consulte le plus
                    souvent, s’il reçoit des messages d’erreur pour certaines pages, etc).</p>
                <p>Ces cookies permettent d’effectuer des mesures de performance, d’établir des statistiques, de
                    connaître les volumes de fréquentation et d’utilisation des divers éléments, etc.</p>
                <p>Les informations ainsi recueillies nous permettent d’améliorer le fonctionnement général du site et
                    de vous fournir un service optimisé.</p>

                <h2>Les cookies de conversion</h2>
                <p>Nous utilisons des cookies dont l'objectif principal est de mesurer l'impact de nos campagnes
                    marketing et de les améliorer. Ils nous permettent d'établir des statistiques concernant les actions
                    d’un internaute après avoir cliqué sur une annonce Wink AE (ex. téléchargement de l’application,
                    inscription, souscription d’un abonnement) Ces cookies n’ont pas de finalité publicitaire.</p>


                <h2>Les cookies de contenus des plateformes tierces</h2>
